function [Rx, Ry] = RK44_4th(F, G, Nx, Ny, dx, dy) 
Rx = zeros(Nx, Ny, 4); Ry = zeros(Nx, Ny, 4);
   
for a = 1:Ny
    if     a == 1          
        Ry(a,:,:) =  (1/dy).* (-1/12.*G(Ny-1,:,:) + 2/3.*G(Ny,:,:) - 2/3.*G(a+1,:,:) + 1/12.*G(a+2,:,:));
    elseif a == 2
        Ry(a,:,:) =   (1/dy).* (-1/12.*G(Ny,:,:) + 2/3.*G(1,:,:) - 2/3.*G(a+1,:,:) + 1/12.*G(a+2,:,:));
    elseif a == Ny 
        Ry(a,:,:) =   (1/dy).* (-1/12.*G(a-2,:,:) + 2/3.*G(a-1,:,:) - 2/3.*G(1,:,:) + 1/12.*G(2,:,:));    
    elseif a == Ny - 1
        Ry(a,:,:) =   (1/dy).* (-1/12.*G(a-2,:,:) + 2/3.*G(a-1,:,:) - 2/3.*G(Ny,:,:) + 1/12.*G(1,:,:));
    else    
        Ry(a,:,:) =   (1/dy).* (-1/12.*G(a-2,:,:) + 2/3.*G(a-1,:,:) - 2/3.*G(a+1,:,:) + 1/12.*G(a+2,:,:));
    end
end

for a = 1:Nx
    if     a == 1          
        Rx(:,a,:) =  (1/dx).* (-1/12.*F(:,Nx-1,:) + 2/3.*F(:,Nx,:) - 2/3.*F(:,a+1,:) + 1/12.*F(:,a+2,:));
    elseif a == 2
        Rx(:,a,:) =   (1/dx).* (-1/12.*F(:,Nx,:) + 2/3.*F(:,1,:) - 2/3.*F(:,a+1,:) + 1/12.*F(:,a+2,:));
    elseif a == Nx 
        Rx(:,a,:) =   (1/dx).* (-1/12.*F(:,a-2,:) + 2/3.*F(:,a-1,:) - 2/3.*F(:,1,:) + 1/12.*F(:,2,:));
    elseif a == Nx - 1
        Rx(:,a,:) =   (1/dx).* (-1/12.*F(:,a-2,:) + 2/3.*F(:,a-1,:) - 2/3.*F(:,Nx,:) + 1/12.*F(:,1,:));
    else 
        Rx(:,a,:) =   (1/dx).* (-1/12.*F(:,a-2,:) + 2/3.*F(:,a-1,:) - 2/3.*F(:,a+1,:) + 1/12.*F(:,a+2,:));
    end
end